void main() {
  int num1 = 15;
  int num2 = 10;
  int num3 = 5;
  int num4 = 20;
  int num5 = 25;

  print(num4--);
  print(num4);
  print(num4--);
  print(num4);

  print(--num5);
  print(num5);
  print(num5);
  print(num5);

  print(++num3);
  print(num3);

  print(num3++);
  print(num3);

  print(num1 + num2);
  print(num1 - num2);
  print(num1 * num2);
  print(-num1);
  print(num1 % num2);
  print(num1 ~/ num2); //integer division
}
