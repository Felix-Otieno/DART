//Bitwise is a level of operation that involves working with individual bits which are the smallest units of data in a computing system.
void main() {
  int num1 = 55;
  int num2 = 78;

  print(num1 & num2); //BITWISE AND &
  print(num1 | num2); //BITWISE OR |
  print(num1 ^ num2); //BITWISE XOR ^
  print(~num2); //BITWISE NOT ~
  print(num1 < 30); //BITWISE LEFT SHIFT <
  print(num1 > 0); //BITWISE RIGHT SHIFT >
}
