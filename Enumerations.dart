// An enumeration is a set of values called as elements, members, etc.

enum TheFamily { Joseph, Oliver, Don, Felix, Stephen, Malik }

void main() {
  print(TheFamily.Joseph.index);
  print(TheFamily.Oliver.index);
  print(TheFamily.Don.index);
  print(TheFamily.Felix.index);
  print(TheFamily.Stephen.index);
  print(TheFamily.Malik.index);
}
